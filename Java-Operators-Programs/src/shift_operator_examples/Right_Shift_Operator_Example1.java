package shift_operator_examples;

public class Right_Shift_Operator_Example1 {
	public static void main(String[] args) {
		//In java the right shift operator can be works division operation, remaining calculation is same as the left shift operator
		System.out.println(10>>2);//10/2^2=10/4=2
		System.out.println(20>>3);//20/2^3=20/8=2
		System.out.println(200>>6);//30/2^5=300/32=
	}
}
