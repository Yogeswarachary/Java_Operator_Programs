package unary_operator_example;

public class Unary_Operator_Example1 {
	public static void main(String[] args) {
		int x=10;
		System.out.println("Post incerement Unary Operator:- "+ x++);
		System.out.println("pre incerement Unary Operator:- "+ ++x);
		System.out.println("Post decrement Unary operator:- "+ x--);
		System.out.println("Pre decrement unary operator:- "+ --x);
	}
}
